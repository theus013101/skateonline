    private void btnFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarActionPerformed
    try {
        double total = 0.0;
        StringBuilder resumo = new StringBuilder();

        // Nome do cliente (usuário logado)
        String nomeCliente = LoginView.usuarioLogado.getNome(); 
        resumo.append("Cliente: ").append(nomeCliente).append("\n\n");

        resumo.append("Itens comprados:\n");

        // Percorre a tabela de produtos
        for (int i = 0; i < tabelaProdutos.getRowCount(); i++) {
            String nome = tabelaProdutos.getValueAt(i, 0).toString();
            double valor = Double.parseDouble(tabelaProdutos.getValueAt(i, 1).toString());
            int quantidade = Integer.parseInt(tabelaProdutos.getValueAt(i, 2).toString());

            if (quantidade > 0) { // só mostra se comprou algo
                double subtotal = valor * quantidade;
                total += subtotal;

                resumo.append("- ")
                      .append(nome)
                      .append(" | Quantidade: ")
                      .append(quantidade)
                      .append(" | Subtotal: R$ ")
                      .append(subtotal)
                      .append("\n");
            }
        }

        resumo.append("\nTotal do pedido: R$ ").append(total);

        // Exibe mensagem de confirmação
        JOptionPane.showMessageDialog(this, resumo.toString());

        // Fecha a tela de pedidos e volta para login
        dispose();
        new LoginView().setVisible(true);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao finalizar pedido: " + e.getMessage());
        e.printStackTrace();
    }
    }//GEN-LAST:event_btnFinalizarActionPerformed

