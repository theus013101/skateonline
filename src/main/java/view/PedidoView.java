private void carregarProdutosNaTabela() {
    DefaultTableModel modelo = (DefaultTableModel) tabelaProdutos.getModel();
    modelo.setRowCount(0); // limpa tabela

    try {
        ProdutosDAO dao = new ProdutosDAO();
        List<Produto> lista = dao.listarProdutos();

        for (Produto p : lista) {
          
            modelo.addRow(new Object[]{
                p.getNome(),
                p.getValor(),
                0 
            });
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao carregar produtos: " + e.getMessage());
        e.printStackTrace();
    }
}


    private void atualizarTotal() {
        double total = 0.0;

        for (int i = 0; i < tabelaProdutos.getRowCount(); i++) {
            String valorStr = tabelaProdutos.getValueAt(i, 1).toString();
            String qtdStr = tabelaProdutos.getValueAt(i, 2).toString();

            if (!valorStr.isEmpty() && !qtdStr.isEmpty()) {
                double valor = Double.parseDouble(valorStr);
                int qtd = Integer.parseInt(qtdStr);
                total += valor * qtd;
            }
        }

        lblTotal.setText("Total: R$ " + total);
    }
